char	*ft_strcat(char *dest, char *src)
{
	int n;
	int i;

	n = 0;
	i = 0;
	while (dest[n])
	{
		n++;
	}
	while (src[i])
	{
		dest[n] = src[i];
		i++;
		n++;
	}
	dest[n] = '\0';
	return (dest);
}
