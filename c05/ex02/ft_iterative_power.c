int	ft_iterative_power(int nb, int power)
{
	int res;
	int i;

	res = nb;
	i = 1;
	if (power == 0)
		return (1);
	if (power < 0)
		return (0);
	while (i < power)
	{
		res *= nb;
		i++;
	}
	return (res);
}
