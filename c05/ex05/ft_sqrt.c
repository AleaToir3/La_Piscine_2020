int	ft_sqrt(int nb)
{
	long int i;

	i = 1;
	if (nb == 1)
		return (1);
	if (nb == 0)
		return (0);
	while (i * i < nb)
	{
		if (i > nb / 2)
			return (0);
		i++;
	}
	if (i * i == nb)
		return ((int)i);
	else
		return (0);
}
