int	ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb <= 1)
		return (0);
	while (i <= nb / i)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	int next;

	next = nb;
	if (nb <= 1)
		return (2);
	while (ft_is_prime(next) == 0)
		next++;
	return (next);
}
