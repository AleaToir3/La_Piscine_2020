#include <unistd.h>

void	ft_putstr(char *str)
{
	int n;

	n = 0;
	while (str[n])
	{
		n++;
	}
	write(1, str, n);
	write(1, "\n", 1);
}

int		ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] && s2[i] && (s1[i] == s2[i]))
		i++;
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}

int		main(int argc, char **argv)
{
	int		i;
	int		j;
	char	*c;

	i = 1;
	while (i < argc)
	{
		j = i + 1;
		while (j < argc)
		{
			if (ft_strcmp(argv[i], argv[j]) > 0)
			{
				c = argv[j];
				argv[j] = argv[i];
				argv[i] = c;
			}
			j++;
		}
		i++;
	}
	i = 1;
	while (i < argc)
		ft_putstr(argv[i++]);
	return (0);
}
