#include <unistd.h>

void	ft_putnbr(int nb)
{
	int reste;

	if (nb == -2147483648)
			write(1, "-2147483648", 11);
	else if (nb < 0)
	{
		nb *= -1;
		write(1, "-", 1);
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	if (nb != -2147483648)
	{
		reste = nb % 10 + 48;
		write(1, &reste,1);
	}
}

int main(void)
{
	ft_putnbr(2147483647);
	return (0);
}
